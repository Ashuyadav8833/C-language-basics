// Variables and Data Types in C
// This program demonstrates the use of different variable types in C.

#include <stdio.h>  // Standard Input-Output library

int main() {
    // Declaring different types of variables
    int age = 21;             // Integer type variable
    float pi = 3.1415;        // Floating-point variable
    char grade = 'A';         // Character variable
    double largeNumber = 1.23456789; // Double precision floating-point variable

    // Printing the values of variables
    printf("Age: %d\n", age);               // %d is used for integers
    printf("Value of Pi: %f\n", pi);        // %f is used for float
    printf("Grade: %c\n", grade);           // %c is used for characters
    printf("Large Number: %lf\n", largeNumber); // %lf is used for double

    return 0;  // Returning 0 indicates successful execution
}
